# ********* format string ************
txt = "My name is John, I am " + "24 years old"
print(txt)

age = 21
txt = f'I am {age} years old' 
print(txt)                   # I am 21 years old
print(f"Age is {age*3:.2f} ") # Age is 63.00

# *********** Concatenate String **********
a = 'Hello'
b = 'World'
c = a +" "+ b
print(c)

# ********* Escape Characters ***********
txt = "We are the \"Vikers\" of Bangladesh"
print(txt)